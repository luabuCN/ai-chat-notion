import { createRequire } from "node:module";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { mkdir, writeFile } from "node:fs/promises";
import { execFile } from "node:child_process";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const require = createRequire(import.meta.url);

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(scriptDir, "..");
const outputPath = resolve(
  packageRoot,
  "src/generated/openui-chat-system-prompt.ts"
);

const entry = require.resolve("@openuidev/react-ui/genui-lib");
const cliEntry = require.resolve("@openuidev/cli/dist/index.js");

const { stdout } = await execFileAsync(
  process.execPath,
  [
    cliEntry,
    "generate",
    entry,
    "--export",
    "openuiChatLibrary",
    "--no-interactive",
  ],
  {
    cwd: packageRoot,
    maxBuffer: 1024 * 1024 * 10,
  }
);

const source = `// This file is generated by \`pnpm --filter @repo/ai generate:openui-prompt\`.
// Do not edit it manually.

export const openuiChatSystemPrompt = ${JSON.stringify(stdout.trim())};
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, source, "utf8");
